a = int(input("Numero 1 : "))
b = int(input("Numero 2 : "))
c = int(input("Numero 3 : "))
if max(a,b,c) == a:
    print("Maior : a")
if max(a,b,c) == b:
    print("Maior : b")
if max(a,b,c) == c:
    print("Maior : c")
if min(a,b,c) == a:
    print("Menor : a")
if min(a,b,c) == b:
    print("Menor : b")
if min(a,b,c) == c:
    print("Menor : c")